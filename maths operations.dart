# Define two variables
a = 10
b = 5

# Addition
sum_result = a + b
print(f"Addition: {sum_result}")

# Subtraction
diff_result = a - b
print(f"Subtraction: {diff_result}")

# Multiplication
prod_result = a * b
print(f"Multiplication: {prod_result}")

# Division
div_result = a / b
print(f"Division: {div_result}")

# Modulus (Remainder)
mod_result = a % b
print(f"Modulus: {mod_result}")

# Exponentiation
exp_result = a ** b
print(f"Exponentiation: {exp_result}")
